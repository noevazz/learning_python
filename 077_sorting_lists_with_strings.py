#  sort lists containing strings

# Demonstrating the sorted() function
# sorted method returns the sorted list (but do NOT modify the list provided as argument)
firstGreek = ['omega', 'alpha', 'pi', 'gamma']
firstGreek2 = sorted(firstGreek)

print(firstGreek)
print(firstGreek2)

print()

# Demonstrating the sort() method
# sort method sorts the list
secondGreek = ['omega', 'alpha', 'pi', 'gamma']
print(secondGreek)

secondGreek.sort()
print(secondGreek)