# and:
# One logical conjunction operator in Python is the word and.
# It's a binary operator with a priority that is lower than the
# one expressed by the comparison operators.
# It allows us to code complex conditions without the use of parentheses like this one:
#  if  counter > 0 and value == 100
# Truth table
# True and True = False
# any other conbination is False


# or:
# A disjunction operator is the word or. It's a binary operator with a lower priority than and (just like + compared to *).
# Its truth table is as follows:
# True  or True	 = T
# True  or False = T
# False or True	 = T
# False or False = F

# not:
# It's a unary operator performing a logical negation.
# Its operation is simple: it turns truth into falsehood and falsehood into truth
# not True  = False
# not False = True

