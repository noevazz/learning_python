# Now you'll learn how to write your own functions, and how to use them.

# DECOMPOSITION: divide the code (or more accurately: the problem) into well-isolated pieces, and encodes each of them in the form of a function.
# This considerably simplifies the work of the program, because each piece of code can be encoded separately, and tested separately.

# Where do the functions come from?
# - Python itself:  numerous functions (like print()) are an integral part of Python,
#   and are always available without any additional effort on behalf of the programmer; we call these functions built-in functions;
# - Modules: the use of these functions requires some additional steps from the programmer in order to make them fully accessible
# - Code: you can write your own functions, place them inside your code, and use them freely